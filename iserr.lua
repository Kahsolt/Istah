iserr = {}			-- 模块名
local _M = iserr	-- 临时模块名

-----------------------------------------------------------------------------
-- Pulic variables & functions
-----------------------------------------------------------------------------
function _M.what(src,msg,fatal)
	print('['..src..' Error]: #'..Cur_Line..':'..Cur_Column..' => '..msg)
	if fatal==0 then os.exit() end
end

-----------------------------------------------------------------------------
return _M
